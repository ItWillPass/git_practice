value_1 = float(input("Enter first vlaue: "))
value_2 = float(input("Enter second vlaue: "))
operation = input('Enter operation: ')

if operation == "/":
    print(value_1 / value_2)
elif operation == "-":
    print(value_1 - value_2)
elif operation == "*":
    print(value_1 * value_2)
elif operation == "+":
    print(value_1 + value_2)